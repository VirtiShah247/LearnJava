/*
Rules for Break statement:
-> When break statement is encountered in loop, loop is immediately terminated and program control resumes at next statement
 after that loop.
-> Break statement can be used in following cases:
    -> Switch case
    -> Inside the loop
    -> Inside the label block

Rules for Continue statement:
-> It is used in loop when you need to jump to next itertion of the loop immediately.
-> It is used to skip current iteration and continue executing next iteration.
-> If we are using it outside the loop, we will get compile error.
*/